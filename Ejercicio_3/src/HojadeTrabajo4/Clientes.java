
package HojadeTrabajo4;


public class Clientes {
    
    int codigo_cliente;
    String nombre;
    String tipo_Cta;
    int no_cta;
    int telefono;

    public Clientes(int codigo_cliente, String nombre, String tipo_Cta, int no_cta, int telefono) {
        this.codigo_cliente = codigo_cliente;
        this.nombre = nombre;
        this.tipo_Cta = tipo_Cta;
        this.no_cta = no_cta;
        this.telefono = telefono;
    }
    
    

    
}
